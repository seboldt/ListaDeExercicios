primeiro = float(input('Informe a nota do primeiro bimestre \n'))
segundo = float(input('Informe a nota do segundo bimestre \n'))
terceiro = float(input('Informe a nota do terceiro bimestre \n'))
quarto = float(input('Informe a nota do quarto bimestre \n'))

